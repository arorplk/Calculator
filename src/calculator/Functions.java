package calculator;

/**
 *
 * @author Pulkit
 */
public class Functions {


    
    public static double add( double ans, double a) {
        return a += ans;
    }

    public static double sub(double ans, double s) {
        return s -= ans;
    }

    public static double mult(double ans, double m) {
        return ans = ans * m;
    }

    public static double dev(double ans, double d) {
        return ans = ans / d;

    }
}
